#include "ft_printf.h"

char	*proc_str(char *s, t_fields *f, char *str)
{
	if (!f || !s || !str)
		return (s);
	return (s);
}

char	*proc_char(char *s, t_fields *f, char c)
{
	if (!f || !s || !c)
		return (s);
	return (s);
}
